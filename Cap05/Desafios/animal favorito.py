# Preguntamos al usuario por su animal favorito
respuesta = input("¿Cuál es tu animal favorito? ")  

# Evaluamos la respuesta usando condicionales
if respuesta == "perro":  
    print("¡Los perros son leales y amigables!")  
elif respuesta == "gato":  
    print("¡Los gatos son jugetones y cariñosos!")
else:  
    print("¡Los " + respuesta + "es son muy inteligentes!")  
